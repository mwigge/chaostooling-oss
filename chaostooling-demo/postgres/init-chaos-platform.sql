-- Function to clean up old audit logs (keep 365 days)
CREATE OR REPLACE FUNCTION chaos_platform.cleanup_old_audit_logs()
RETURNS void AS $$
DELETE FROM chaos_platform.audit_log
WHERE action_timestamp < NOW() - INTERVAL '365 days';
$$ LANGUAGE SQL;

-- Function to archive old experiment runs (optional, mark as archived)
CREATE OR REPLACE FUNCTION chaos_platform.archive_old_runs()
RETURNS void AS $$
UPDATE chaos_platform.experiment_runs
SET status = 'archived'
WHERE end_time < NOW() - INTERVAL '180 days'
AND status = 'completed';
$$ LANGUAGE SQL;

