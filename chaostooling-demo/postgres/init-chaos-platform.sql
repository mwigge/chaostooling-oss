-- Experiment Runs Summary View
-- Shows all experiment runs with complete metadata and scores in separate columns
CREATE OR REPLACE VIEW chaos_platform.experiment_runs_summary AS
SELECT
    er.run_id,
    er.experiment_id,
    e.experiment_name,
    s.service_id,
    s.service_name,
    er.run_number,
    er.status,
    er.start_time,
    er.end_time,
    er.risk_score,
    er.complexity_score,
    er.test_quality,
    er.risk_level,
    er.success_rate,
    er.steady_state_passed,
    er.experiment_completed,
    er.recovery_successful,
    er.result_summary,
    er.metadata,
    er.executed_by,
    er.environment,
    er.created_at
FROM
    chaos_platform.experiment_runs er
    JOIN chaos_platform.experiments e ON er.experiment_id = e.experiment_id
    JOIN chaos_platform.services s ON e.service_id = s.service_id
ORDER BY
    er.start_time DESC;

-- Experiment Audit Trail View
-- Shows complete audit log entries with experiment context
CREATE OR REPLACE VIEW chaos_platform.experiment_audit_trail AS
SELECT
    al.log_id,
    al.entity_type,
    al.entity_id AS run_id,
    er.experiment_id,
    e.experiment_name,
    COALESCE(al.details->>'title', e.experiment_name) AS experiment_title,
    s.service_name,
    er.run_number,
    al.action AS audit_action,
    al.details AS audit_details,
    al.actor AS audit_actor,
    al.action_timestamp AS audit_timestamp
FROM
    chaos_platform.audit_log al
    LEFT JOIN chaos_platform.experiment_runs er ON al.entity_id = er.run_id AND al.entity_type = 'experiment_run'
    LEFT JOIN chaos_platform.experiments e ON er.experiment_id = e.experiment_id
    LEFT JOIN chaos_platform.services s ON e.service_id = s.service_id
WHERE
    al.entity_type = 'experiment_run'
ORDER BY
    al.action_timestamp DESC;

-- Latest Experiment Runs View
-- Shows only the most recent run for each experiment
CREATE OR REPLACE VIEW chaos_platform.latest_experiment_runs AS
SELECT DISTINCT ON (er.experiment_id)
    er.run_id,
    er.experiment_id,
    e.experiment_name,
    s.service_name,
    er.run_number,
    er.status,
    er.start_time,
    er.end_time,
    er.risk_score,
    er.complexity_score,
    er.test_quality,
    er.risk_level,
    er.success_rate,
    er.steady_state_passed
FROM
    chaos_platform.experiment_runs er
    JOIN chaos_platform.experiments e ON er.experiment_id = e.experiment_id
    JOIN chaos_platform.services s ON e.service_id = s.service_id
ORDER BY
    er.experiment_id,
    er.start_time DESC;

